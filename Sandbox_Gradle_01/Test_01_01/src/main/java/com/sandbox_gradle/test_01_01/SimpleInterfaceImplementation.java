        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        this.textOne = textOne;
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("SimpleInterface, setTextOne ended!!!" + textOne);
