package com.sandbox_gradle.test_01_01;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Lazy;
import org.springframework.stereotype.Component;

@Component("lazyClass")
@Lazy//CLASS(COMPONENT) AND BEANS MUST BE MARKED @LAZY,
// BOTH OF THEM, ONLY THEN THEY WILL BE CALLED ONLY IN CLASS TEST3(WHERE WE WORK WITH THEM IMMEDIATELY)
public class LazyClass {
    private final String eeny;
    private final String meeny;
    private String miny;
    private String burn;

    public LazyClass(String eeny, String meeny){
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, constructor initialization start!!!");
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        this.eeny = eeny;
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, first constructor parameter!!!");
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        this.meeny = meeny;
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, constructor initialization end!!!" + eeny + " " + meeny);
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
    }

    @Autowired
    public void setMiny(String miny) {
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, setMiny call!!!");
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        this.miny = miny;
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, setMiny end!!!" + miny);
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
    }

    public String getMiny(){
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, getMiny call!!!");
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        return miny;
    }

    @Autowired
    public void setBurn(String burn) {
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, setBurn call!!!");
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        this.burn = burn;
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, setBurn end!!!" + burn);
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        this.miny = miny;
    }

    public String getBurn(){
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        System.out.println("LazyClass, getBurn call!!!");
        System.out.println("/////////////////////////////////////////////////////////////////////////////////////////");
        return burn;
    }
}
